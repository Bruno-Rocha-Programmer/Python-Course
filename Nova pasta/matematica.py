import math

# Valor absolute

print("Valor absoluto de -5 é {}".format(abs(-5))) # 5
print(abs(5)) # 5

# Potenciação

print("Potenciação do valor 3 é {}".format(pow(3, 2)))

# Arrendondando valores

print("Arrendondando o valor 3.5 é {}".format(round(3.5))) # 4

# Raiz quadrada

print(f"Raiz quadrada de 9 é {math.sqrt(9)}")

# Arrendondar para cima e para baixo
# Para cima

print(f"O valor 2.3 arrendondado para cima é {math.ceil(2.3)}")

# Para baixo

print(f"O valor 2.3 arrendondado para baixo é {math.floor(2.3)}")

# Logaritmo
logaritmo = math.log(100, 10) # log base 10
print(f"O logaritmo de 100 na base 10 é {logaritmo}")

# Valor de pi

print(f"O valor de pi é {math.pi}")

# Fatorial

print(f"O fatorial de 8 é {math.factorial(8)}")

# Dividir valor por infinito

print(f"Dividir 10 por infinito é {4 / math.inf}")